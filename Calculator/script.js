function Operations(j) {
    let result = 0;
    switch(j){
        case "+":
            result = a + b;
            break;
        case "-":
            result = a - b;
            break;
        case "*":
            result = a*b;
            break;
        case "/":
            result = a/b;
            break;
    }
    return result;
}



